//Basic operators

//Arithmetic operators

let wallet = 10;

console.log(wallet + 4);
console.log(wallet - 1);
console.log(wallet * 4);
console.log(wallet % 2);
console.log(wallet ** 2);

// Increment operator

wallet++
console.log(wallet);

wallet+=20
console.log(wallet);

//Decrement operator

wallet--
console.log(wallet);

wallet-=30;
console.log(wallet);

